#include "3-calc.h"

/**
 * op_add - add 2 ints.
 * @a: first int.
 * @b: second int.
 * Return: result.
 */

int op_add(int a, int b)
{
	int result;

	result = a + b;

	return (result);
}

/**
 * op_sub - substaract 2 ints.
 * @a: first int.
 * @b: second int.
 * Return: result.
 */

int op_sub(int a, int b)
{
	int result;

	result = a - b;

	return (result);
}

/**
 * op_mul - multiply 2 ints.
 * @a: first int.
 * @b: second int.
 * Return: result.
 */

int op_mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}

/**
 * op_div - divise 2 ints.
 * @a: first int.
 * @b: second int.
 * Return: result.
 */

int op_div(int a, int b)
{
	int result;

	result = a / b;

	return (result);
}

/**
 * op_mod - reminder of division of 2 ints.
 * @a: first int.
 * @b: second int.
 * Return: result.
 */

int op_mod(int a, int b)
{
	int result;

	result = a % b;

	return (result);
}
